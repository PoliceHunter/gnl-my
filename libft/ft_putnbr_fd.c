#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char index;

	index = 0;
	if (n == -2147483648)
	{
		n = n / 10;
		index = 1;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n < 10)
		ft_putchar_fd(n + '0', fd);
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (index == 1)
		ft_putchar_fd(8 + '0', fd);
}
