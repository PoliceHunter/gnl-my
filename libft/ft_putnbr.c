#include "libft.h"

void	ft_putnbr(int n)
{
	char index;

	index = 0;
	if (n == -2147483648)
	{
		n = n / 10;
		index = 1;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (index == 1)
		ft_putchar(8 + '0');
}
